class AttentionBlock(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.query = Linear(in_channels, out_channels)
        self.key = Linear(in_channels, out_channels)
        self.value = Linear(in_channels, out_channels)
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        query = self.query(x)
        key = self.key(x)
        value = self.value(x)

        attn_weights = self.softmax(torch.bmm(query, key.transpose(1, 2)))
        attended_values = torch.bmm(attn_weights, value)

        return attended_values

